enum TypeEnum {
    numericfill = 1,
    alfafill = numericfill << 1,
    signedNumber = alfafill << 1,
    plusIfPositive = signedNumber << 1,
    spaceIfPositive = plusIfPositive << 1,
    leftJustify = spaceIfPositive << 1,
    centerJustify = leftJustify << 1,
    upcase = centerJustify << 1,
    alternative = upcase << 1,
    nice = alternative << 1,
    widthDynamic = nice << 1,
    precisionDynamic = widthDynamic << 1,
    widthGiven = precisionDynamic << 1,
    precisionGiven = widthGiven << 1,
    active = precisionGiven << 1
};

struct FormatState
{
    const char* prefix;
    const char* suffix;
    unsigned width;
    unsigned precision;
    unsigned position;
    unsigned widthPosition;
    unsigned precisionPosition;
    unsigned prefixSize;
    unsigned short type;
    char formatSpecifier;
    char fillCharacter;

    mutable char buf[31]; // enough for 5 flags, 2 ints, a dot and a specifier.

    FormatState() = default;

    void parse(const char*& format);

    const char* unParse() const;

    bool widthGiven() const
    {
        return type & TypeEnum::widthGiven;
    }

    bool precisionGiven() const
    {
        return type & TypeEnum::precisionGiven;
    }

    bool widthDynamic() const
    {
        return type & TypeEnum::widthDynamic;
    }

    bool precisionDynamic() const
    {
        return type & TypeEnum::precisionDynamic;
    }

    bool active() const
    {
        return type & TypeEnum::active;
    }

    void setWidthGiven(bool v = true)
    {
        if (v) {
            type |= TypeEnum::widthGiven;
        } else {
            type &= ~TypeEnum::widthGiven;
        }
    }

    void setPrecisionGiven(bool v = true)
    {
        if (v) {
            type |= TypeEnum::precisionGiven;
        } else {
            type &= ~TypeEnum::precisionGiven;
        }
    }

    void setWidthDynamic(bool v = true)
    {
        if (v) {
            type |= TypeEnum::widthDynamic;
        } else {
            type &= ~TypeEnum::widthDynamic;
        }
    }

    void setPrecisionDynamic(bool v = true)
    {
        if (v) {
            type |= TypeEnum::precisionDynamic;
        } else {
            type &= ~TypeEnum::precisionDynamic;
        }
    }

    void setActive(bool v = true)
    {
        if (v) {
            type |= TypeEnum::active;
        } else {
            type &= ~TypeEnum::active;
        }
    }

    void reset()
    {
        prefix = nullptr;
        suffix = nullptr;
        width = 0;
        precision = 0;
        position = 0;
        widthPosition = 0;
        precisionPosition = 0;
        prefixSize = 0;
        type = 0;
        formatSpecifier = 0;
        fillCharacter = ' ';
    }
};

struct FormatNode
{
    FormatNode* next;
    FormatNode* child;
    const char* format;
    FormatState state;

    FormatNode() = default;

    void reset()
    {
        next = nullptr;
        child = nullptr;
        format = nullptr;
        state.reset();
    }
};

struct FormatNodes
{
    static constexpr size_t chunckSize = 64;
    std::array<FormatNode, chunckSize> nodes;
    FormatNodes* next = nullptr;
    size_t index = 0;
};

struct Format
{
    Format(std::string& d, const char* f) : format(f), wholeFormat(f), dest(d)
    {
    }

    ~Format()
    {
        auto next = chuncks;

        while (next != &nodes) {
            auto tmp = next;

            next = next->next;
            delete tmp;
        }
    }

    FormatNode* getNode();
    FormatNode* buildTree(bool& positional);
    static void printTree(std::ostream& os, FormatNode* node, unsigned indent);
    void dump();

    struct StackElement
    {
        StackElement(FormatNode* n, size_t c) : node(n), count(c)
        {
        }

        FormatNode* node;
        size_t count;
    };

    void push(size_t count)
    {
        if (count != 0) {
            stack.emplace_back(nextNode, count - 1);
        }
    }

    void showErrorContext(FormatNode* node) const;

#if __cplusplus < 201703L
    void errorTail()
    {
    }


    template<typename T, typename... Ts>
    void errorTail(const T& t, const Ts&... ts)
    {
        std::cout << t;
        errorTail(ts...);
    }

    template<typename... Ts>
    void error(const Ts&... ts)
    {
        if (!errorGiven) {
            std::cerr << "TSIO error: ";
            errorTail(ts...);
            std::cerr << ".\n";
            showErrorContext(nextNode);

            errorGiven = true;
        }
    }

    template<typename... Ts>
    void error(FormatNode* node, const Ts&... ts)
    {
        if (!errorGiven) {
            std::cerr << "TSIO error: ";
            errorTail(ts...);
            std::cerr << ".\n";
            showErrorContext(node);

            errorGiven = true;
        }
    }

#else
    template<typename... Ts>
    void error(const Ts&... ts)
    {
        if (!errorGiven) {
            std::cerr << "TSIO error: ";
            (std::cerr << ... << ts);
            std::cerr << ".\n";
            showErrorContext(nextNode);

            errorGiven = true;
        }
    }

    template<typename... Ts>
    void error(FormatNode* node, const Ts&... ts)
    {
        if (!errorGiven) {
            std::cerr << "TSIO error: ";
            (std::cerr << ... << ts);
            std::cerr << ".\n";
            showErrorContext(node);

            errorGiven = true;
        }
    }
#endif

    void getNextNode(bool first = false);
    FormatNode* getNextSibling(FormatNode* node, bool first = false);
    void skipToFormat();
    void tabTo(unsigned column, bool absolute);

    const char* format;
    const char* wholeFormat;
    std::string& dest;
    std::vector<StackElement> stack;
    FormatNode* nextNode = nullptr;
    FormatNodes nodes;
    FormatNodes* chuncks = &nodes;
    bool errorGiven = false;
};

